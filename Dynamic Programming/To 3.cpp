void testcase( ) {
    ll x; cin >> x;
    string str = to_string(x);
    int n = str.size( );
    vector<vii> dp(n + 1 , vii(3));
    dp[0][1] = dp[0][2] = 1000000000;
    dp[0][0] = n;
    for (int i = 1; i <= n; i++) {
        for (int r = 0; r < 3; r++) {
            dp[i][r] = dp[i - 1][r];
            int x = str[i - 1] - '0';
            x %= 3;
            int r1 = r - x + 3;
            r1 %= 3;
            dp[i][r] = min(dp[i][r] , dp[i - 1][r1] - 1);
        }
    }

    cout << (dp[n][0] < n ? dp[n][0] : -1);





}


